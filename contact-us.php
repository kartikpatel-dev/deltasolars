function chkrequired()
{
	if(document.mailform.name.value=="")
	{
		alert("Please Enter Name !!!");
		document.mailform.name.focus();
		return false;
	}

	
	if(document.mailform.phone.value=="")
	{
		alert("Please Enter Phone no. !!!");
		document.mailform.phone.focus();
		return false;
	}
	
	
	if(document.mailform.email.value=="")
	{
		alert("Please Enter E-Mail !!!");
		document.mailform.email.focus();
		return false;
	}
	else
	{
		var pass=document.mailform.email.value.indexOf('@',0);
		var pass1=document.mailform.email.value.indexOf('.',pass);
		if((pass==-1) || (pass1==-1))
		{
			alert ("Please Enter E-Mail in Proper Format !!!");
			document.mailform.email.focus();
			return false;
		}
		if(pass+1==pass1)
		{
			alert ("Please Enter E-Mail in Proper Format !!!");
			document.mailform.email.focus();
			return false;
		}
	}
	
	if(document.mailform.msg.value=="")
	{
		alert("Please Enter Message!!!");
		document.mailform.msg.focus();
		return false;
	}		
	
	if(document.mailform.v_code.value=="")
	{
		alert("Please Enter Verification Code !!!");
		document.mailform.v_code.focus();
		return false;
	}
	
	return true;
}
